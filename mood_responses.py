

def mood_response (x):
    mood = x.lower()    
    if (mood == "happy"):
        print("Oh Happy Dayyy!")
    elif (mood == "sad"):
        print("Oh no! I hope the sun brightens up your day!")
    elif (mood == "excited"):
        print("OMG I'M SOO EXCITED")
    elif (mood == "angry"):
        print("Okay.. Let's take some deep breaths")
